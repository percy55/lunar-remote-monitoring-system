CREATE TABLE readings (
inverter VARCHAR(10) NOT NULL,
recordedat DATETIME NOT NULL,
ac_volts INT(8) NOT NULL,
ac_current INT(8) NOT NULL,
ac_freq REAL(2.1) NOT NULL,
heatsink_temp INT(3) NOT NULL,
pv1_volts INT(8) NOT NULL,
pv2_volts INT(8) NOT NULL,
pv1_current INT(8) NOT NULL,
pv2_current INT(8) NOT NULL,
watts INT(8) NOT NULL);
